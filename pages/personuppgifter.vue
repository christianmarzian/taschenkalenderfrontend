<h4 class="subtitle is-4">Rätt till tillgång:</h4>
<p>Du har rätt att begära och få bekräftelse på om Bolaget behandlar personuppgifter om dig. Om Bolaget behandlar personuppgifter om dig har du rätt att kostnadsfritt få information om behandlingen samt en kopia av de personuppgifterna som är under behandling. För eventuellt ytterligare kopior äger Bolaget rätt att ta ut en rimlig avgift baserat på Bolagets administrativa kostnader för sådana kopior. Om begäran är uppenbart ogrundad eller orimlig får Bolaget, i enlighet med vad som följer av tillämplig dataskyddslagstiftning, ta ut en rimlig avgift för sådan begäran eller vägra tillmötesgå sådan begäran.</p>
<h4 class="subtitle is-4">Rätt till rättelse av felaktiga personuppgifter:</h4>
<p>Du har rätt att utan onödigt dröjsmål få felaktiga personuppgifter om dig rättade. Beroende på ändamålet med behandlingen har du även rätt att komplettera ofullständiga personuppgifter.</p>
<h4 class="subtitle is-4">Rätt till radering ("rätten att bli bortglömd"):</h4>
<p>Du har i vissa fall rätt att få dina personuppgifter raderade, t.ex.</p>
<ul>
	<li>om personuppgifterna inte längre är nödvändiga för de ändamål för vilka de samlats in;</li>
	<li>om behandlingen grundar sig på ditt samtycke har du rätt att när som helst återkalla ditt samtycke och därefter rätt till radering av uppgifterna (förutsatt att det inte finns någon annan rättslig grund för behandlingen);</li>
	<li>om du invänder mot behandling som grundar sig på en intresseavvägning och det saknas berättigade skäl för behandlingen som väger tyngre (du har dock alltid rätt till radering av personuppgifter som används för direkt marknadsföring med stöd av en intresseavvägning);</li>
	<li>om personuppgifterna har behandlats på ett olagligt sätt eller de måste raderas p.g.a. rättslig förpliktelse;</li>
</ul>
<p>Rätten till radering gäller dock inte i vissa fall, t.ex. om behandlingen är nödvändig för att Bolaget ska uppfylla en rättslig förpliktelse eller för att Bolaget ska kunna fastställa, göra gällande eller försvara rättsliga anspråk.</p>
<h4 class="subtitle is-4">Rätt till begränsning:</h4>
<p>Du har i vissa fall rätt att begära att Bolaget begränsar behandlingen av dina personuppgifter, t.ex. om du inte anser att personuppgifterna är korrekta så att Bolaget får tid att kontrollera detta, om behandlingen är olaglig och du inte vill att vi raderar uppgifterna, om Bolaget inte längre behöver uppgifterna men du behöver dem för rättsliga anspråk eller om du invänder mot Bolagets behandling med stöd av en intresseavvägning i väntan på kontroll av om Bolagets eller tredje parts berättigade skäl väger tyngre än dina berättigade skäl.</p>
<h4 class="subtitle is-4">Rätt att göra invändningar:</h4>
<p>Du har rätt att när som helst göra invändningar mot behandling av dina personuppgifter som grundar sig på en intresseavvägning, inbegripet profilering. Bolaget får inte fortsätta att behandla sådana personuppgifter såvida Bolaget inte kan presentera tvingande berättigade skäl för sådan behandling som väger tyngre än dina intressen, rättigheter och friheter. Bolaget får dock fortsätta behandlingen för fastställande, utövande eller försvar av rättsliga anspråk. Om du invänder mot behandling för direkt marknadsföring som sker med stöd av en intresseavvägning får Bolaget inte längre behandla dina personuppgifter för sådant ändamål. Du kan när som helst avstå från att ta emot marknadsföringskommunikation från oss genom att klicka på "avsluta prenumeration" i e-post och SMS kommunikation som vi skickar dig.</p>
<h4 class="subtitle is-4">Rätt till dataportabilitet:</h4>
<p>Du har i vissa fall rätt att överföra dina personuppgifter till en annan personuppgiftsansvarig, t.ex. om behandlingen grundar sig på ett samtycke eller på ett avtal mellan dig och Bolaget samt under förutsättning att behandlingen sker automatiserat. När det är tekniskt möjligt ska sådan överföring ske direkt från Bolaget till en annan personuppgiftsansvarig.</p>
<h4 class="subtitle is-4">Rätt att inge klagomål till Datainspektionen:</h4>
<p>Om du anser att dina rättigheter inte respekteras av oss får du gärna kontakta oss och du har även en rätt att lämna in ett klagomål till Integritetsskyddsmyndigheten.</p>

